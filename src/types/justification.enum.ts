/**
 * Represents the justification of an element along an axis.
 * 
 * @param left-
 *    Alignment element to the left
 * 
 * @param center-
 *    Alignment element to the center
 * 
 * @param right-
 *    Alignment element to the right
 */
export enum Justification {
  left = -1,
  center = 0,
  right = 1
}